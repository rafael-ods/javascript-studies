// DECLARAÇÃO DE FUNÇÃAO (FUNCTION  HOISTING)
// NADA MAIS É QUE A FUNÇÃO PADRAO DE DECLARAÇÃO, E O MODO QUE PODEMOS CHAMAR ELA É TANTO DEPOIS DA SUA DECLARAÇÃO OU ANTES EX:
talkHi() //CHAMANDO ANTES E PODEMOS CHAMAR DEPOIS TBM

function talkHi() {
    console.log('Hello')
}

//FUNCAO EM JAVASCRIPT SAO FIRST-CLASS OBJECTS (OBJETOS DE PRIMERIA CLASSE)
// SEGUE O MODO DE DECLARAÇÃO ABAIXO, QUE NADA MAIS É DO QUE FUNCTION EXPRESSION
const talkHi2 = function () {
    console.log('Hello2')
}
talkHi2()
//JA QUE ELA ESTA EM UMA VARIAVEL PODEMOS UTILIZAR ELA DENTRO DE OUTRA FUNCAO SEGUE EX:

function getFunction(execution) {
    execution() //NOTA QUE ESTOU CHAMANDO UM PARAMETRO E EXECUTANDO COMO FUNÇÃO
}
getFunction(talkHi2) // E CHAMANDO NOSSA CONST FUNCTION EXPRESSION

//TEMOS ARROW FUNCTION QUE NADA MAIS É QUE A SINTAXE DE UMA SETA = () => SEGUE EX:

const talkHi3 = () => {
    console.log('Hello3')
}
talkHi3()